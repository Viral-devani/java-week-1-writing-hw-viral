package homework_week_5;
/*
        * 10. Write a Java program that takes two numbers as input and display the
        * product of two numbers.
        * Test Data:
        * Input first number: 25
        * Input second number: 5
        * Expected Output : 25 x 5 = 125
        */

public class ProductOfTwoNmubers {
    public static void main(String[] args) {
        //First number declaration
        int firstnumber = 25;
        //Second number declaration
        int seconnumber = 5;
        int produced = firstnumber * seconnumber;
        //output
        System.out.println(firstnumber +  "  X  " + seconnumber + "  = " + produced );
    }
}
